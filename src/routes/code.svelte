<p>Language(s): Lua</p>
<p><a href="https://github.com/jamesb93/REAPER-Scripts">https://github.com/jamesb93/REAPER-Scripts</a></p>

<h2 id="z12-rack-implementation">z12 (Rack implementation)</h2>
<p>Miller Puckette outlines an algorithm for generating stochastic sequences that are maximally uniform in this paper. This repo contains a Rack module that implements this algorithm. The output is somewhat markovian in taste and allows you to create some varied rhythms through an interface for managing patterns probabilistically.</p>
<p>Language(s): C++</p>
<p><a href="https://github.com/jamesb93/z12">https://github.com/jamesb93/z12</a></p>

