<p>
    Stitch/Strata was composed using audio descriptors and Python scripts to carve out small samples from a corpus of vocal segments. These segments are drawn from <a href="https://sagepbbbt.com/daily-sketches/">Sage Pbbbt's daily sketches.</a> The first performance was at the 2019 Electric Spring Festival, where I was generously offered the opportunity to diffuse it over a 3D array of speakers.
</p>

<p>
    The code for this project was mostly made in an ad-hoc fashion and is largely untraceable. Nonetheless it is available <a href="https://github.com/jamesb93/Stitch-Strata">here</a> for the brave.
</p>
