<h2>Intersymmetric</h2>

<p>
    Intersymmetric Sequencer 1 is an online multiplayer drum machine that I built with Mark Fell, Rian Treanor and Joe Gilmore. It uses a shared interface for manipulating pattern generation and sound synthesis. This enables groups of people to play together in real time. 
</p>

<p>
    The ideas behind the project came into focus during the COVID lockdowns as an alternative to media streaming and other traditional approaches to web based delivery. It also draws upon longer term interests in interactive pattern generation in sound and music from electronic dance music traditions. 
</p>

