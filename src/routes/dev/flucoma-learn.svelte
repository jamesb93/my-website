<h1>Fluid Corpus Manipulation Learn Platform</h1>

<p>
    As part of my role on the <a target='_blank' href='https://flucoma.org'>Fluid Corpus Manipulation (FluCoMa)</a> project I devise (with amazing colleagues) a series of docs and resources  to enable techno-fluent musicians with our toolkit. Toward this, I am responsible for designing and maintaining a framework enabling our team to author content for the <a target='_blank' href='https://learn.flucoma.org'>FluCoMa Learn Platform</a>. The platform's goal is to provide a rich resource that hosts explanations of FluCoMa toolkit algorithms, tutorials and community portraits of advanced use-cases. Where possible we support this content with interactive widgets and related multimedia. All of this content is indexed in a searchable database, aiding discovery and navigation. 
</p>

<p>
    The website uses <a target='_blank' href='https://kit.svelte.dev/'>SvelteKit</a> to and <a target='_blank' href='https://mdsvex.pngwn.io/'>MDsveX</a>
</p>

<p>
</p>

<p>
    The deployment of the site is backed by git and github actions, deploying the tip of main to our digital ocean droplet. This makes collaborative contributions and editing have minimal friction for our team.
</p>